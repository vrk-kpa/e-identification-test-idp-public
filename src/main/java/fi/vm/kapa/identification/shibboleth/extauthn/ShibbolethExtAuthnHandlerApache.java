package fi.vm.kapa.identification.shibboleth.extauthn;

import fi.vm.kapa.identification.shibboleth.extauthn.authn.ApacheAuthnHandler;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.beans.factory.config.AutowireCapableBeanFactory;
import org.springframework.web.context.WebApplicationContext;
import org.springframework.web.context.support.WebApplicationContextUtils;

import javax.servlet.ServletConfig;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import java.io.IOException;


@WebServlet(name = "ShibbolethExtAuthnHandlerApache", urlPatterns = {"/authn/External/*"})
public class ShibbolethExtAuthnHandlerApache extends HttpServlet {

    @Autowired
    private transient ApacheAuthnHandler handler;

    @Override
    public void init(ServletConfig config) throws ServletException {
        WebApplicationContext springContext = WebApplicationContextUtils.getRequiredWebApplicationContext(config.getServletContext());
        final AutowireCapableBeanFactory beanFactory = springContext.getAutowireCapableBeanFactory();
        beanFactory.autowireBean(this);
    }

    @Override
    public void service(final HttpServletRequest httpRequest, final HttpServletResponse httpResponse)
            throws ServletException, IOException {

        handler.initialize(httpRequest, httpResponse);
    }

}